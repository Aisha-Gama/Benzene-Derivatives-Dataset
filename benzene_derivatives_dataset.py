import pandas as pd
from rdkit import Chem
from rdkit.Chem import Descriptors

benzene_derivatives = [
    "C1=CC=CC=C1",       # Benzene
    "CC1=CC=CC=C1",      # Toluene
    "C1=CC=C(C=C1)O",    # Phenol
    "C1=CC=C(C=C1)N",    # Aniline
    "C1=CC=C(C=C1)[N+](=O)[O-]",  # Nitrobenzene
    "C1=CC=C(C=C1)C=O",  # Benzaldehyde
    "C1=CC=C(C=C1)C(=O)O",  # Benzoic Acid
    "C1=CC=C(C=C1)Cl",   # Chlorobenzene
    "C1=CC=C(C=C1)Br",   # Bromobenzene
    "C1=CC=C(C=C1)I",    # Iodobenzene
    "CC1=CC=CC=C1",      # Ethylbenzene
    "C=CC1=CC=CC=C1",    # Styrene
    "CC(=O)C1=CC=CC=C1", # Acetophenone
    "C1=CC=C(C=C1)C#N",  # Benzonitrile
    "C1=CC(=C(C=C1)O)O", # Catechol
    "C1=CC=C(C=C1)C(=O)N", # Benzamide
    "C1=CC=C(C=C1)C(=O)C", # Acetylbenzene
    "C1=CC=C(C=C1)CC",   # Propylbenzene
    "C1=CC=C(C=C1)CO",   # Benzyl Alcohol
    "C1=CC=C(C=C1)C=C",  # Vinylbenzene
    "C1=CC=C(C=C1)CCl",  # Chloromethylbenzene
    "C1=CC=C(C=C1)CF3",  # Trifluorotoluene
    "C1=CC=C(C=C1)CBr",  # Bromomethylbenzene
    "C1=CC=C(C=C1)COC",  # Benzyl Ether
    "C1=CC=C(C=C1)CN",   # Benzylamine
    "C1=CC=C(C=C1)CNO",  # Benzonitrone
    "C1=CC=C(C=C1)CN(C)C",  # Dimethylbenzylamine
    "C1=CC=C(C=C1)CON",  # Benzoyl Amide
    "C1=CC=C(C=C1)CONH2", # Benzamide
    "C1=CC=C(C=C1)CNC",  # Benzylamine derivative
]

# List to store molecular properties
data = []

# Loop through each SMILES and calculate properties
for smiles in benzene_derivatives:
    molecule = Chem.MolFromSmiles(smiles)
    if molecule:  # Ensure molecule is valid
        molecular_weight = Descriptors.MolWt(molecule)
        logp = Descriptors.MolLogP(molecule)
        num_h_donors = Descriptors.NumHDonors(molecule)
        num_h_acceptors = Descriptors.NumHAcceptors(molecule)

        # Append the properties to the data list
        data.append({
            "SMILES": smiles,
            "Molecular Weight": molecular_weight,
            "LogP": logp,
            "H-Donors": num_h_donors,
            "H-Acceptors": num_h_acceptors
        })

# Convert the data to a pandas DataFrame
df = pd.DataFrame(data)

# Save the dataset as a CSV file
df.to_csv("benzene_derivatives_dataset.csv", index=False)

# Display the dataset
print(df)

